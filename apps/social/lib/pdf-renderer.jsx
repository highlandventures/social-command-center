// ============================================================
// KPI VALUE FORMATTING
// ============================================================

function formatKPIValue(kpi) {
  const v = kpi.value;
  if (v == null) return '\u2014';
  switch (kpi.format) {
    case 'number':
      return typeof v === 'number' ? v.toLocaleString() : String(v);
    case 'percent':
      return `${v}%`;
    case 'delta':
      return `${v > 0 ? '+' : ''}${v}`;
    case 'text':
      return String(v);
    default:
      return typeof v === 'number' ? v.toLocaleString() : String(v);
  }
}

function formatDelta(delta) {
  if (delta == null) return null;
  const sign = delta > 0 ? '+' : '';
  return `${sign}${delta}%`;
}

